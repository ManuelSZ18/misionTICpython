import random
x = random.randrange(1, 10, 1);
y = random.randrange(1, 10, 1);
z = random.randrange(1, 10, 1);
t = random.randrange(1, 10, 1);

ecuacion = x + (2*y) - z + (3*t)

print('Variable X: {}'.format(x))
print('Variable Y: {}'.format(y))
print('Variable Z: {}'.format(z))
print('Variable T: {}'.format(t))
print('x + 2y - z + 3t = {}'.format(ecuacion))